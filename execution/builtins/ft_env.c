#include "builtins.h"

int ft_env(char **env)
{
	int i;

	i = 0;
	if (!env)
		return (0);
	while (env[i])
	{
		if (env[i][0] != '\0')
			printf("%s\n", env[i]);
		i++;
	}
	return (0);
}
